const slugify = (name) =>
  name.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');

// Equirectangular projection to map-image percentages.
export const lonToX = (lon) => ((lon + 180) / 360) * 100;
export const latToY = (lat) => ((90 - lat) / 180) * 100;

export const CITY_DATABASE = RAW.map(([name, tz, lat, lon]) => ({
  id: slugify(name),
  name,
  tz,
  lat,
  lon,
  x: lonToX(lon),
  y: latToY(lat),
}));
